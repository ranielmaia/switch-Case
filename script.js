let temporada = "Primavera";

switch (temporada) {
    case temporada = "Verão":
        console.log("Opte por roupas leves e beba água!");
        break;
    case temporada = "Outono":
        console.log("Melhor levar um casaco.")
        break;
    case temporada = "Inverno":
        console.log("Tem que usar 3 peças de roupa.")
        break;
    case temporada = "Primavera":
        console.log("Só uma camiseta serve.")
        break;
    default:
        console.log("Isso não é uma estação.")
}